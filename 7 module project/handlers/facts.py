def register_facts_handlers(dp):
    @dp.message(F.text.in_(["📊 Факты"]))
    async def facts(message: Message):
        user_id = message.from_user.id
        text = (
            "📡 КЛИМАТИЧЕСКАЯ СВОДКА ООН\n\n"
            "Здесь собраны реальные данные из докладов Межправительственной группы экспертов по изменению климата (IPCC) и Программы ООН по окружающей среде (UNEP).\n\n"
            "Каждый факт — это выжимка из сотен страниц научных отчётов. Никаких домыслов. Только то, что подтверждено спутниками, ледяными кернами и замерами температуры за последние 150 лет.\n\n"
            "🎲 Нажми на кнопку ниже — получи случайный факт\n\n"
            "👇 Один клик — и ты узнаешь чуть больше о планете, чем раньше"
        )
        await message.answer(text, reply_markup=random_fact_keyboard)
    
    @dp.callback_query(F.data == "random_fact")
    async def send_random_fact(callback: CallbackQuery):
        fact = random.choice(facts_list)
        await callback.answer()
        await callback.message.answer(fact)

